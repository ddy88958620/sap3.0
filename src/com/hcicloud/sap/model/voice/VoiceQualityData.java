package com.hcicloud.sap.model.voice;

/**
 * 用户
 * @author lixianji
 *
 */
public class VoiceQualityData{
	/**
	 * 主键
	 */
	private String uuid;
	/**
	 * 名称
	 */
	private String qualityData;
	
    /**
     * 获取uuid  
     * @return the uuid
     */
    public String getUuid() {
        return uuid;
    }
    /**
     * 设置uuid  
     * @param uuid the uuid to set
     */
    public void setUuid(String uuid) {
        this.uuid = uuid;
    }
    /**
     * 获取qualityData  
     * @return the qualityData
     */
    public String getQualityData() {
        return qualityData;
    }
    /**
     * 设置qualityData  
     * @param qualityData the qualityData to set
     */
    public void setQualityData(String qualityData) {
        this.qualityData = qualityData;
    }
}
